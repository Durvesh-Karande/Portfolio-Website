// ===== Build Photo HTML =====
function buildGrid(section) {
    const grid = document.getElementById(section.gridId);
    const countEl = document.getElementById(section.countId);
    if (!grid) return;

    let html = '';
    section.files.forEach((file, i) => {
        const src = section.basePath + file;
        const delay = (i * 0.05).toFixed(2);
        html += `
        <div class="photo-item" data-title="${section.title}" data-desc="${section.desc}" style="animation-delay:${delay}s">
            <div class="photo-wrap">
                <img src="${src}" alt="${section.title}" loading="lazy">
                <div class="photo-overlay">
                    <div class="photo-info"><h4>${section.title}</h4><p>${section.desc}</p></div>
                    <button class="photo-zoom"><i class="fas fa-expand"></i></button>
                </div>
                <span class="photo-cat-badge ${section.badgeClass}">${section.badge}</span>
            </div>
        </div>`;
    });

    grid.innerHTML = html;
    grid.classList.add('has-photos');

    if (countEl) countEl.textContent = section.files.length + ' Photos';

    // Attach lightbox click
    grid.querySelectorAll('.photo-item').forEach(item => {
        item.querySelector('.photo-wrap').addEventListener('click', () => openFromItem(item));
        const zoom = item.querySelector('.photo-zoom');
        if (zoom) zoom.addEventListener('click', e => { e.stopPropagation(); openFromItem(item); });
    });
}

// ===== Init all sections =====
sections.forEach(buildGrid);

