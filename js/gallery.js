// ===== Quick Jump smooth scroll =====
document.querySelectorAll('.qj-btn').forEach(link => {
    link.addEventListener('click', e => {
        e.preventDefault();
        const target = document.querySelector(link.getAttribute('href'));
        if (target) {
            const offset = 130; // account for sticky navbar + filter bar + quickjump
            const top = target.getBoundingClientRect().top + window.scrollY - offset;
            window.scrollTo({ top, behavior: 'smooth' });
        }
    });
});

// ===== Filter =====
function updateQuickJump(filter) {
    document.querySelectorAll('.qj-btn').forEach(btn => {
        const cat = btn.dataset.cat;
        const show = filter === 'all' || cat === filter;
        btn.classList.toggle('hidden', !show);
    });
}

document.querySelectorAll('.ftab').forEach(btn => {
    btn.addEventListener('click', () => {
        document.querySelectorAll('.ftab').forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        const filter = btn.dataset.filter;

        document.querySelectorAll('.category-section').forEach(section => {
            const match = filter === 'all' || section.dataset.cat === filter;
            section.style.display = match ? '' : 'none';
        });

        updateQuickJump(filter);
    });
});

updateQuickJump('all');

// ===== Lightbox =====
const lightbox  = document.getElementById('lightbox');
const lbImg     = document.getElementById('lbImg');
const lbTitle   = document.getElementById('lbTitle');
const lbDesc    = document.getElementById('lbDesc');
const lbCounter = document.getElementById('lbCounter');
const lbClose   = document.getElementById('lbClose');
const lbPrev    = document.getElementById('lbPrev');
const lbNext    = document.getElementById('lbNext');
const lbOverlay = document.getElementById('lightboxOverlay');

let allVisibleItems = [];
let currentIndex    = 0;

function getVisibleItems() {
    return [...document.querySelectorAll('.category-section:not([style*="none"]) .photo-item')];
}

function openFromItem(item) {
    allVisibleItems = getVisibleItems();
    currentIndex    = allVisibleItems.indexOf(item);
    showLightbox(currentIndex);
}

function showLightbox(index) {
    if (!allVisibleItems.length) return;
    currentIndex = (index + allVisibleItems.length) % allVisibleItems.length;
    const item   = allVisibleItems[currentIndex];
    lbImg.src    = item.querySelector('img').src;
    lbTitle.textContent   = item.dataset.title || '';
    lbDesc.textContent    = item.dataset.desc  || '';
    lbCounter.textContent = (currentIndex + 1) + ' / ' + allVisibleItems.length;
    lightbox.classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeLightbox() {
    lightbox.classList.remove('open');
    document.body.style.overflow = '';
    lbImg.src = '';
}

if (lbClose)   lbClose.addEventListener('click', closeLightbox);
if (lbOverlay) lbOverlay.addEventListener('click', closeLightbox);
if (lbPrev)    lbPrev.addEventListener('click', e => { e.stopPropagation(); showLightbox(currentIndex - 1); });
if (lbNext)    lbNext.addEventListener('click', e => { e.stopPropagation(); showLightbox(currentIndex + 1); });

document.addEventListener('keydown', e => {
    if (!lightbox.classList.contains('open')) return;
    if (e.key === 'Escape')     closeLightbox();
    if (e.key === 'ArrowLeft')  showLightbox(currentIndex - 1);
    if (e.key === 'ArrowRight') showLightbox(currentIndex + 1);
});

let touchStartX = 0;
lightbox.addEventListener('touchstart', e => { touchStartX = e.touches[0].clientX; });
lightbox.addEventListener('touchend',   e => {
    const diff = touchStartX - e.changedTouches[0].clientX;
    if (Math.abs(diff) > 50) showLightbox(diff > 0 ? currentIndex + 1 : currentIndex - 1);
});
